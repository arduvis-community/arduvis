def _fltmode_fields(options, vehicle):
    """Generate the 6 FLTMODE select fields for a given vehicle."""
    defaults = {1: 0, 2: 5, 3: 6, 4: 2, 5: 2, 6: 6}  # sane ArduPilot defaults
    return [
        {
            "key": f"fltmode{n}", "label": f"Mode {n}  (FLTMODE{n})",
            "type": "select", "required": True,
            "default": defaults.get(n, 0),
            "vehicle": vehicle,
            "options": options,
        }
        for n in range(1, 7)
    ]

